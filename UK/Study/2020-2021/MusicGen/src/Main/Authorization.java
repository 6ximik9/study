    static void PlugIn() {
        DB a = new DB();
        conn = null;
        if (conn == null) {
            conn = a.Connect();
            if (conn == null) {
                pnlActiv.setBackground(Color.red);
                btnUpdateCon.setVisible(true);
                btnLogin.setEnabled(false);
                return;
            } else {
                pnlActiv.setBackground(Color.GREEN);
                btnUpdateCon.setVisible(false);
                btnLogin.setEnabled(true);
            }
        }
    }

