    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        DB db = new DB();
        if (statBtnNext == 0) //Увійти
        {
            if (txtLogin.getText().length() > 0 && txtPass.getText().length() > 0) {

                X = db.GetUser("users", txtLogin.getText(), txtPass.getText());
                try {

                    if (X.next() == true) {
                        JOptionPane.showMessageDialog(rootPane, "Успішно", "Вхід", 1);
                        frmMain.setNameUser(txtLogin.getText());
                        frmMain m = new frmMain();
                        frmMain.setNameUser(txtLogin.getText());
                        m.setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Неправильний логін або пароль", "Помилка данних", 2);
                    }
                    db.finalize();
                } catch (SQLException ex) {
                    Logger.getLogger(Authorization.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Заповніть всі поля", "Помилка данних", 2);
            }
        } else if (statBtnNext == 1) //Зареєструватись
        {
            if (txtLogin.getText().length() > 0 && txtPass.getText().length() > 0 && txtPib.getText().length() > 0) {
                db.GetR("users", txtLogin.getText());
                if (getNameRpt() == "") {
                    db.Register("users", txtPib.getText(), txtLogin.getText(), txtPass.getText());
                    JOptionPane.showMessageDialog(rootPane, "Успішно", "Реєстрація", 1);
                    lblTitle.setText("Авторизація");
                    btnBack.setVisible(false);
                    btnLogin.setText("Авторизуватись");
                    lblRegister.setVisible(false);
                    lblPib.setVisible(false);
                    txtPib.setVisible(false);
                    jSeparator4.setVisible(false);
                    txtLogin.setText("");
                    txtPass.setText("");
                    lblRegister.setVisible(true);
                    statBtnNext = 0;
                    db.finalize();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Користувач існує", "Дублювання даних", 2);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Заповніть всі поля", "Помилка данних", 2);
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed


