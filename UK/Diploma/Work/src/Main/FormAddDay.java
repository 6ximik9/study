    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (listPerson.size() > 0 && allW.getText().length() > 0 && countPerson > 0 && MyDate.getDate() != null) {
            String result = listPerson.stream()
                    .map(n -> String.valueOf(n))
                    .collect(Collectors.joining());
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            double allWe = Double.parseDouble(allW.getText());
            double one = allWe / countPerson;
            BigDecimal bd = new BigDecimal(Double.toString(one));
            bd = bd.setScale(3, RoundingMode.HALF_UP);
            if (variantStart == 0) {
                a.MyInsert("allWork", dateFormat.format(MyDate.getDate()), countPerson, result, bd.doubleValue(), allWe);
                JOptionPane.showMessageDialog(this, "Успішно", "Додавання", 1);
                FormMain.GetData("allWork");
            } else if (variantStart == 1) {
                a.Update("allWork", dateFormat.format(MyDate.getDate()), countPerson, result, bd.doubleValue(), allWe, id);
                JOptionPane.showMessageDialog(this, "Успішно", "Редагування", 1);
                FormMain.GetData("allWork");
            }
            countPerson = 0;
            listEdit.clear();
            listPerson.clear();
            variantStart = 0;
            FormMain.setAddP(0);
            a.finalize();
            dispose();
        } else if (MyDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Дату не вибрано", "Помилка", 2);
        } else if (listPerson.size() <= 0 || countPerson == 0) {
            JOptionPane.showMessageDialog(this, "Не вибрано робочих", "Помилка", 2);
        } else if (allW.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Не вказано кількість кілограм", "Помилка", 2);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

