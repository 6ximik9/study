/**
 * Створення кнопки
 * @param name Ім'я
 * @param variant Варіант кліку
 * @return 
 */
    JButton CreateBtn(String name, int variant) {
        JButton b = new JButton(name);
        b.setPreferredSize(new Dimension(200, 50));
        b.setBackground(Color.WHITE);
        Font BigFontTR = new Font("TimesRoman", Font.BOLD, 14);
        b.setFont(BigFontTR);
        b.setVisible(true);
        b.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent e) {

                if (e.getButton() == MouseEvent.BUTTON3) {
                    b.setBackground(Color.WHITE);
                    if (listPerson.indexOf(b.getText() + "\n") != -1) {
                        listPerson.remove(listPerson.indexOf(b.getText() + "\n"));
                        countPerson -= 1;
                    }

                } else if (e.getButton() == MouseEvent.BUTTON1) {
                    b.setBackground(new Color(120, 101, 84));
                    if (listPerson.indexOf(b.getText() + "\n") == -1) {
                        countPerson += 1;
                        listPerson.add(b.getText() + "\n");
                    }
                }
            }
        });

        return b;
    }

